package has_a_relationship;

public class Addres {
	String street;
	String location;
	String mandal;
	String district;
	String state;
	int pincode;
	public Addres(String street, String location, String mandal, String district, String state, int pincode) {
	
		this.street = street;
		this.location = location;
		this.mandal = mandal;
		this.district = district;
		this.state = state;
		this.pincode = pincode;
	}

}


